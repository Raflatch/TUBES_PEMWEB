                <h2>Diazepam</h2>
                <p><em>23 Jun 2020</em></p>
                <img src="../image/obat_dua.jpg" alt="Diazepam Image">
                <h3>Penjelasan</h3>
                <p>Diazepam adalah obat yang termasuk dalam kelompok benzodiazepin. Obat ini bekerja dengan 
                    cara meningkatkan aktivitas neurotransmitter tertentu di otak.</p>
                <p>Obat ini bisa kamu gunakan untuk mengobati gangguan kecemasan, atau gejala penarikan alkohol. 
                    ereka terkadang juga bisa kamu gunakan bersama dengan obat lain untuk mengobati kejang dan 
                    kekakuan otot.</p>
                <p>Obat ini hanya bisa kamu dapatkan dengan resep dokter. Obat ini hadir dalam bentuk tablet, 
                    cairan atau supositoria (obat yang kamu gunakan dengan cara kamu masukkan ke dalam anus).</p>
            <h3>Manfaat Diazepam</h3>
            <ul>
                <li>Mengobati gangguan kecemasan.</li>
                <li>Meredakan gejala yang terjadi akibat penarikan alkohol, seperti tremor, berkeringat atau sulit tidur.</li>
                <li>Mengatasi kejang dan kekakuan otot.</li>
                <li>Membantu pasien untuk rileks sebelum operasi atau perawatan medis atau gigi lainnya.</li>
            </ul>

            <h3>Dosis Diazepam</h3>
            <p>Dosis obat ini berbeda untuk setiap pasien. Pastikan untuk mengikuti dosis yang dokter anjurkan 
                atau petunjuk penggunaan yang tertera pada kemasan saat mengonsumsi obat ini. Berikut dosis obat 
                tersebut secara umum:</p>
            <p><strong>Untuk bentuk oral (larutan atau tablet)</strong></p>
            <ul>
                <li><strong>Untuk mengatasi kecemasan</strong>
                    <ul>
                        <li><strong>Dewasa:</strong> 2-10 miligram (mg), 2 sampai 4 kali sehari.</li>
                        <li><strong>Orang dewasa yang lebih tua:</strong> Awalnya, 2-2,5 mg, 1 atau 2 kali sehari. Dokter mungkin akan meningkatkan dosisnya bila perlu.</li>
                        <li><strong>Anak-anak usia 6 bulan ke atas:</strong> Awalnya, 1 hingga 2,5 mg, 3 atau 4 kali per hari. Dokter anak bisa meningkatkan dosis bila perlu.</li>
                        <li><strong>Anak-anak di bawah usia 6 bulan:</strong> Penggunaan tidak dokter anjurkan.</li>
                    </ul>
                </li>
                <li><strong>Untuk gejala penarikan alkohol</strong>
                    <ul>
                        <li><strong>Dewasa:</strong> 10 mg, 3 atau 4 kali selama 24 jam pertama, kemudian 5 mg 3 sampai 4 kali per hari sesuai kebutuhan.</li>
                        <li><strong>Orang dewasa yang lebih tua:</strong> Awalnya, 2-2,5 mg 1 atau 2 kali sehari. Dokter secara bertahap akan meningkatkan dosis sesuai kebutuhan.</li>
                    </ul>
                </li>
                <li><strong>Untuk kejang otot</strong>
                    <ul>
                        <li><strong>Dewasa:</strong> 2-10 mg, 3 atau 4 kali sehari.</li>
                        <li><strong>Orang dewasa yang lebih tua:</strong> Awalnya, 2-2,5 mg, 1 atau 2 kali sehari. Dokter mungkin akan meningkatkan dosis bila perlu.</li>
                        <li><strong>Anak-anak usia 6 bulan ke atas:</strong> Awalnya, 1-2,5 mg, 3 atau 4 kali per hari. Dokter anak bisa meningkatkan dosis bila perlu.</li>
                        <li><strong>Anak-anak di bawah usia 6 bulan:</strong> Penggunaan tidak dokter anjurkan.</li>
                    </ul>
                </li>
                <li><strong>Untuk kejang</strong>
                    <ul>
                        <li><strong>Dewasa:</strong> 2-10 mg, 2 sampai 4 kali sehari.</li>
                        <li><strong>Orang dewasa yang lebih tua:</strong> Awalnya, 2-2,5 mg, 1 atau 2 kali sehari. Dokter mungkin akan meningkatkan dosis bila perlu.</li>
                        <li><strong>Anak-anak usia 6 bulan ke atas:</strong> Awalnya, 1-2,5 mg, 3 atau 4 kali per hari. Dokter anak bisa meningkatkan dosis bila perlu.</li>
                        <li><strong>Anak-anak di bawah usia 6 bulan:</strong> Penggunaan tidak dokter anjurkan.</li>
                    </ul>
                </li>
            </ul>
            <p><strong>Untuk bentuk supositoria:</strong> Dosis akan dokter tentukan berdasarkan berat badan, usia dan kesehatan pasien secara umum.</p>

            <h3>Cara Penggunaan Diazepam</h3>
            <p>Konsumsi obat ini persis seperti yang sudah dokter tentukan atau sesuai petunjuk yang tertera pada label. Jangan gunakan obat dalam jumlah yang lebih besar, atau gunakan lebih lama dari yang direkomendasikan.</p>
            <p>Berikut cara menggunakan obat ini:</p>
            <ul>
                <li>Untuk bentuk tablet, telan tablet secara utuh. Jangan menghancurkan atau mengunyahnya.</li>
                <li>Untuk bentuk cair, gunakan sendok atau cangkir pengukur yang ada pada kemasan agar dosisnya tepat. Jangan gunakan sendok makan untuk mengukur dosis obat.</li>
                <li>Obat ini harus kamu gunakan hanya untuk waktu yang singkat. Jangan minum obat ini lebih dari 4 bulan tanpa saran dokter.</li>
                <li>Jangan berhenti minum obat tanpa bertanya terlebih dahulu dengan dokter.</li>
            </ul>
