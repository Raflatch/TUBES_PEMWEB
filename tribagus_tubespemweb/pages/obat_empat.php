                <h2>Lorazepam</h2>
                <p><em>23 Jun 2020</em></p>
                <img src="../image/obat_empat.jpg" alt="Lorazepam Image">
                <h3>Penjelasan</h3>
                <p>Lorazepam adalah obat yang digunakan untuk meredakan kecemasan. Lorazepam juga digunakan untuk mengobati insomnia yang disebabkan oleh kecemasan atau stres situasional sementara.</p>
                <p>Obat ini termasuk dalam kelas obat yang disebut benzodiazepin, yang bekerja dengan cara memperlambat aktivitas di otak untuk memungkinkan relaksasi.</p>
                <h3>Manfaat Lorazepam</h3>
                <ul>
                    <li>Gangguan kecemasan.</li>
                    <li>Kecemasan terkait dengan gejala depresi.</li>
                    <li>Menghilangkan insomnia (masalah tidur) karena kecemasan atau stres situasional.</li>
                    <li>Kecemasan terkait operasi (diberikan sesaat sebelum anestesi).</li>
                    <li>Status epileptikus (kejang yang berlangsung lebih dari 5 menit atau lebih dari satu kali kejang dalam rentang waktu 5 menit).</li>
                </ul>
                <p>Lorazepam termasuk golongan obat yang disebut benzodiazepin. Lorazepam bekerja dengan meningkatkan kadar GABA (gamma-aminobutyric acid) di otak. Para peneliti telah menunjukkan bahwa GABA dapat meningkatkan suasana hati dan memiliki efek relaksasi yang menenangkan pada sistem saraf pusat.</p>
                <p>Peningkatan kadar GABA membantu mengurangi kecemasan dan menghentikan kejang. Selain itu, manfaat lorazepam lainnya adalah:</p>

                <h3>Dosis Lorazepam</h3>
                <p>Lorazepam hadir dalam bentuk tablet dengan dosis 0,5 mg, 1 mg, dan 2,5 mg.</p>
                <ul>
                    <li><strong>Untuk gangguan kecemasan pada orang dewasa:</strong> 1 mg – 4 mg setiap hari.</li>
                    <li><strong>Untuk masalah tidur pada orang dewasa:</strong> 1 mg – 2 mg sebelum tidur. Lorazepam akan mulai bekerja dalam waktu sekitar 20 hingga 30 menit.</li>
                    <li><strong>Untuk anak usia 12 hingga 17 tahun:</strong> 1 mg – 4 mg.</li>
                    <li><strong>Untuk orang dengan masalah hati atau ginjal:</strong> Dokter dapat merekomendasikan dosis yang lebih rendah.</li>
                </ul>

                <h3>Cara Penggunaan Lorazepam</h3>
                <p>Minumlah tablet lorazepam dengan segelas air. Kamu bisa meminumnya dengan atau tanpa makanan. Jika kamu mengonsumsi lorazepam dalam bentuk cair, obatnya akan disertakan dengan jarum suntik atau sendok plastik untuk membantu mengukur dosis yang tepat.</p>
                <p>Jika tidak memiliki jarum suntik atau sendok, tanyakan pada apoteker untuk mendapatkan ukuran yang sesuai. Jangan gunakan sendok teh dapur karena tidak akan mengukur jumlah yang tepat.</p>
