                <h2>Primidone</h2>
                <p><em>23 Jun 2020</em></p>
                <img src="../image/obat_lima.jpg" alt="Primidone Image">
                <h3>Penjelasan</h3>
                <p>Primidone adalah obat untuk meredakan dan mengontrol kejang. Dengan begitu, pasien bisa beraktivitas dengan normal dan risiko terjadinya komplikasi akibat kejang yang berulang bisa menurun. Selain itu, obat ini juga bisa digunakan dalam pengobatan tremor.</p>
                <p>Primidone termasuk ke dalam golongan obat antikejang barbiturat. Obat ini akan dimetabolisme menjadi phenobarbital dan phenylethylmalonamide (PEMA), yang akan mempengaruhi aktivitas kelistrikan di sistem saraf pusat. Cara kerja ini akan membantu meredakan kejang, termasuk kejang saat tidur.</p>

                <h3>Cara Mengonsumsi Primidone dengan Benar</h3>
                <p>Konsumsi primidone sesuai anjuran dokter dan baca aturan pakai yang terdapat di kemasan. Primidone dapat dikonsumsi sebelum atau sesudah makanan. Telan tablet primidone dengan segelas air.</p>
                <p>Pastikan untuk mengonsumsi primidone pada waktu yang sama tiap harinya agar obat bisa bekerja dengan efektif. Jangan berhenti menggunakan obat secara mendadak karena dapat memperparah kondisi yang diderita.</p>
                <p>Apabila lupa mengonsumsi primidone, segera mengonsumsinya jika jeda dengan jadwal waktu konsumsi berikutnya belum terlalu dekat. Bila sudah dekat, abaikan dan jangan menggandakan dosis selanjutnya.</p>
                <p>Selama menjalani pengobatan dengan primidone, Anda akan menjalani pemeriksaan darah lengkap, tes kepadatan tulang, tes kadar asam folat, serta monitoring gejala depresi. Ikuti saran dan jadwal pemeriksaan yang diberikan oleh dokter sehingga kondisi Anda bisa terpantau.</p>
                <p>Simpan Primidone dalam wadah tertutup di tempat kering yang sejuk. Jauhkan obat ini dari jangkauan anak-anak.</p>

                <h3>Interaksi Primidone dengan Obat Lain</h3>
                <p>Berikut ini adalah beberapa efek interaksi antarobat yang dapat terjadi jika primidone digunakan bersama obat tertentu:</p>
                <ul>
                    <li>Penurunan kadar antidepresan trisiklik, penghambat beta, carbamazepine, clozapine, kortikosteroid, cyclophosphamide, ciclosporin, dicoumarin, digitoxin, doxycycline, ethosuximide, etoposide, granisetron, lamotrigine, losartan, methadone, mianserin, montelukast, nimodipine, pil KB, oxcarbazepine, phenytoin, quinidine, rocuronium, tiagabine, topiramate, vecuronium, warfarin, atau zonisamide di dalam tubuh.</li>
                    <li>Peningkatan kadar primidone dalam darah jika digunakan dengan chloramphenicol, felbamate, nelfinavir, metronidazole, atau sodium valproate.</li>
                    <li>Peningkatan risiko terjadinya kerusakan hati jika digunakan dengan paracetamol.</li>
                    <li>Peningkatan risiko terjadinya efek samping jika digunakan bersama obat golongan opioid atau barbiturat lain.</li>
                </ul>
