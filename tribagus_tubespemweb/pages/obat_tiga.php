                <h2>Chlordiazepoxide</h2>
                <p><em>23 Jun 2020</em></p>
                <img src="../image/obat_tiga.jpg" alt="Chlordiazepoxide Image">
                <h3>Penjelasan</h3>
                <p>Chlordiazepoxide adalah obat yang digunakan untuk menghilangkan kecemasan 
                    dan untuk mengontrol agitasi yang disebabkan oleh penarikan alkohol. 
                    Chlordiazepoxide termasuk dalam kelas obat yang disebut benzodiazepin. 
                    Jenis obat ini bekerja dengan mengurangi aktivitas listrik abnormal di otak.</p>
                <p>Chlordiazepoxide hadir dalam bentuk tablet dan kapsul untuk diminum. 
                    Biasanya diminum satu sampai empat kali sehari dengan atau tanpa makanan. 
                    Ikuti petunjuk pada label resep dengan hati-hati. Mintalah dokter atau apoteker 
                    untuk menjelaskan cara bagian mana pun yang tidak dimengerti.</p>
            <h3>Manfaat Chlordiazepoxide</h3>
            <ul>
                <li>Mengobati kecemasan dan penarikan alkohol akut.</li>
                <li>Menghilangkan rasa takut dan kecemasan sebelum operasi.</li>
            </ul>
            <p>Obat ini termasuk golongan obat yang disebut benzodiazepin yang bekerja pada otak dan saraf (sistem saraf pusat) untuk menghasilkan efek menenangkan. Cara kerjanya dengan meningkatkan efek bahan kimia alami tertentu dalam tubuh (GABA).</p>

            <h3>Dosis Chlordiazepoxide</h3>
            <p>Chlordiazepoxide tersedia dalam beberapa dosis, yaitu 5 mg, 10 mg, 25 mg, dan 100 mg.</p>
            <ul>
                <li><strong>Kondisi kecemasan ringan sampai sedang:</strong> 5 atau 10 mg, diminum 3-4 kali sehari.</li>
                <li><strong>Kondisi kecemasan berat:</strong> 20 atau 25 mg, diminum 3-4 kali sehari.</li>
            </ul>
            <p>Meski begitu, dosis harus disesuaikan dengan kebutuhan masing-masing. Kemanjuran manajemen jangka panjang pada pasien dengan kecemasan umumnya membutuhkan lebih dari 4 bulan, tergantung kondisi.</p>

            <h3>Cara Penggunaan Chlordiazepoxide</h3>
            <p>Cara penggunaan obat ini untuk dewasa adalah 5 mg â€“ 25 mg tiga atau empat kali sehari. Orang dewasa yang lebih tua 5 mg dua sampai empat kali sehari. Anak-anak berusia 6 tahun ke atas 5 miligram (mg) dua hingga empat kali sehari. Dokter meningkatkan dosis jika diperlukan.</p>
            <p>Simpan obat ini di dalam wadahnya, tertutup rapat, dan jauhkan dari jangkauan anak-anak. Simpan pada suhu kamar dan jauh dari panas dan kelembapan berlebih (bukan di kamar mandi).</p>
            <p>Penting untuk menjauhkan semua obat dari pandangan dan jangkauan anak-anak. Sebab, banyak wadah (seperti tempat penyimpanan pil mingguan dan untuk obat tetes mata, krim, tambalan, dan inhaler), yang mudah dibuka oleh anak kecil.</p>
            <p>Obat-obatan yang tidak dibutuhkan harus dibuang dengan cara khusus untuk memastikan bahwa hewan peliharaan, anak-anak, dan orang lain tidak dapat mengonsumsinya.</p>
            <p>Kamu tidak boleh membuang obat ini ke toilet. Sebaliknya, cara terbaik untuk membuang obat adalah melalui program pengembalian obat. Bicaralah dengan apoteker atau hubungi departemen sampah/daur ulang setempat untuk mempelajari tentang pembuangan obat yang benar.</p>
